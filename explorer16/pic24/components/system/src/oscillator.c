void OSC_Init(void)
{
    // NOSC PRIPLL; SOSCEN disabled; OSWEN Switch is Complete; 
    __builtin_write_OSCCONL((uint8_t) (0x0300 & 0x00FF));
    // RCDIV FRC/2; DOZE 1:8; DOZEN disabled; ROI disabled; 
    CLKDIV = 0x0000;
    // TUN Center frequency; 
    OSCTUN = 0x0000;
}

